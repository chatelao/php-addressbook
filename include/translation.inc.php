function msg($value)
{
	global $lang, $messages;
		
	if(isset($messages[$value][$lang])) {
	  return $messages[$value][$lang];
	} else {
	  return $value;
	}
}

function ucfmsg($value) {
	
	$msg = msg($value);
	return mb_strtoupper($msg[0]).substr($msg, 1);
	
}
